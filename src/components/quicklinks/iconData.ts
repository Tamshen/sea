export function searchIcons(query: string): { category: string; icon: string }[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  const results: { category: string; icon: string }[] = [];
  for (const cat of iconCategories) {
    for (const icon of cat.icons) {
      if (icon.toLowerCase().includes(q)) {
        results.push({ category: cat.name, icon });
      }
    }
  }
  return results;
}
